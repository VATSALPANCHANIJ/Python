# continue statement:
# Continue statement is used when you want to skip a particular condition

# Break Statement:
# Break statements is used when you want to destroy a loop at a certain condition and cone out of the loop


# Continue Statement
for i in range (1,11):
    if i == 6:
     continue
    else:
        print(i)

# Break Statement
for i in range (1,11):
    if i == 8:
     break
    else:
        print(i)